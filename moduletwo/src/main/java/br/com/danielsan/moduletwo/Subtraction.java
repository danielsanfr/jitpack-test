public final class Subtraction {

    /**
     * Constructor
     */
    private Subtraction() { }

    /**
     * Make subtraction
     * @param a first argument of the operation
     * @param b second argument of the operation
     * @return the subtraction (a - b)
     */
    public static int sub(int a, int b) {
        return a - b;
    }

}
