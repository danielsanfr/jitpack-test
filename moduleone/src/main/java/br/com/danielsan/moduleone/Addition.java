public final class Addition {

    /**
     * Constructor
     */
    private Addition() { }

    /**
     * Make addition
     * @param a first argument of the operation
     * @param b second argument of the operation
     * @return the addition (a + b)
     */
    public static int add(int a, int b) {
        return a + b;
    }

}
